    public IActionResult ReturnGallon()
    {
        return View();
    }



    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////// Search Customer Modal ////////////////////////////////// 
    ////////////////////////////////////////////////////////////////////////////////////////
    public IActionResult ConfirmPayment()
    {
        return View();
    }
}
